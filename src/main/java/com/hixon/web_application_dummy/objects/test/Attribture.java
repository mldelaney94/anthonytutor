package com.hixon.web_application_dummy.objects.test;

public class Attribture {
	private String key;
	private Object pair;
	
	public Attribture(String key_, Object pair_) {
		super();
		this.key = key_;
		this.pair = pair_;
	}
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public Object getPair() {
		return pair;
	}
	public void setPair(Object pair) {
		this.pair = pair;
	}
	
	

}
